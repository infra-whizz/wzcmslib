// Get file content
func (cnt *WzPyPce) Get(name string) []byte {
	content := cnt.data[name]
	if cnt.compressed {
		gz, _ := gzip.NewReader(bytes.NewReader(content))
		defer gz.Close()
		content, _ = ioutil.ReadAll(gz)
	}

	return content
}
